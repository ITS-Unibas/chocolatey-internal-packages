  fileType       = 'exe'
  silentArgs     = '/S'
  validExitCodes = @(0, 3010, 1605, 1614, 1641)
  softwareName   = 'Zotero'
  url64bit       = 'https://download.zotero.org/client/release/7.0.12/Zotero-7.0.12_x64_setup.exe'
  checksum64     = 'f327edd422b8f200e3702b46ed1589e6bc2360828c99f5bfd90fb882b6a1f73f'
  checksumType64 = 'sha256'
}

&(Join-Path $PSScriptRoot InitialScript.ps1)
Install-ChocolateyPackage @packageArgs
&(Join-Path $PSScriptRoot FinalScript.ps1)
